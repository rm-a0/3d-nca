class MultiTaskMorphRunner(NCARunner):
    """Pool runner for task-conditioned 3D-NCA metamorphogenesis."""

    def __init__(self, verbose: bool = True) -> None:
        self.verbose          = verbose
        self.model: Optional[Grid3D] = None
        self.optimizer        = None
        self.targets: List[Tensor] = []
        self.state: Optional[Tensor] = None
        self.current_epoch    = 0
        self.total_epochs     = 0
        self.latest_loss      = 0.0
        self._cell_cfg: Optional[CellConfig] = None
        self._device: Optional[str] = None
        self._pool: List[Tensor] = []
        self._pool_tasks: List[int] = []   # task id per pool slot
        self._batch_size      = 4
        self._lr_scheduler    = None
        self._latest_metrics: Dict[str, float] = {}

    # ── NCARunner interface ───────────────────────────────────────────────────

    def init(self, config: dict, target: "np.ndarray | List[np.ndarray]") -> None:
        if not isinstance(target, list):
            target = [target]
        n = len(target)

        # Patch task_channels into cell config
        cell_cfg_d = {**config["cell"], "task_channels": n}
        self._cell_cfg   = CellConfig(**cell_cfg_d)
        perc_cfg         = PerceptionConfig(**config["perception"])
        upd_cfg          = UpdateConfig(**config["update"])
        grid_cfg         = GridConfig(**config["grid"])
        self._device     = "cuda" if torch.cuda.is_available() else "cpu"

        self.model = Grid3D(
            self._cell_cfg, perc_cfg, upd_cfg, grid_cfg
        ).to(self._device)

        self.optimizer = torch.optim.AdamW(
            self.model.parameters(),
            lr=config["training"]["learning_rate"],
            weight_decay=1e-5,
        )
        self._lr_scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(
            self.optimizer,
            T_max=config["training"]["num_epochs"],
            eta_min=1e-5,
        )

        vis = self._cell_cfg.visible_channels
        self.targets = [self._prep(t, vis) for t in target]

        # Balanced pool: each task gets approximately equal representation.
        self._pool, self._pool_tasks = [], []
        for i in range(_POOL_SIZE):
            tid    = i % n
            t_ids  = torch.tensor([tid], device=self._device)
            seed   = self.model.seed_center(1, self._device, t_ids)
            self._pool.append(seed)
            self._pool_tasks.append(tid)

        self.state         = self._pool[0]
        self.current_epoch = 0
        self.total_epochs  = config["training"]["num_epochs"]
        self._batch_size   = config["training"].get("batch_size", 4)
        self.latest_loss   = 0.0

    def train(
        self, schedule: Optional[Schedule] = None
    ) -> Generator[Dict[str, Any], None, None]:
        for epoch in range(1, self.total_epochs + 1):
            metrics            = self._step()
            self.current_epoch = epoch
            self.latest_loss   = metrics["loss_total"]
            self._latest_metrics = {
                k: float(v) for k, v in metrics.items()
                if isinstance(v, (int, float))
            }
            if schedule is not None:
                schedule.check_and_execute(epoch, self)
            yield metrics

    def snapshot(self) -> TrainingSnapshot:
        return TrainingSnapshot(
            state            = self.state.detach().cpu().numpy().astype(np.float32),
            epoch            = self.current_epoch,
            total_epochs     = self.total_epochs,
            loss             = self.latest_loss,
            visible_channels = self._cell_cfg.visible_channels,
            metrics          = dict(self._latest_metrics),
        )

    def set_target(self, target: Any) -> None:
        pass  # N/A — targets are fixed at init for multi-task training.

    # ── Internals ─────────────────────────────────────────────────────────────

    def _get_step_range(self) -> tuple:
        """Curriculum ramp: steps increase from (8,16) → (32,64) over 2000 epochs."""
        t  = min(self.current_epoch / max(_CURRICULUM_EPOCHS, 1), 1.0)
        lo = int(_STEP_MIN_START + t * (_STEP_MIN_END - _STEP_MIN_START))
        hi = int(_STEP_MAX_START + t * (_STEP_MAX_END - _STEP_MAX_START))
        return max(lo, 4), max(hi, lo + 1)

    def _step(self) -> Dict[str, Any]:
        dev      = self._device
        cell_cfg = self._cell_cfg
        vis      = cell_cfg.visible_channels
        bs       = min(len(self._pool), self._batch_size)
        lo, hi   = self._get_step_range()
        n_steps  = random.randint(lo, hi)

        # Sample a random batch from pool
        indices    = random.sample(range(len(self._pool)), bs)
        batch      = torch.cat([self._pool[i] for i in indices], dim=0)  # (B,C,D,H,W)
        task_ids_b = [self._pool_tasks[i] for i in indices]
        tgt_batch  = torch.cat(
            [self.targets[tid] for tid in task_ids_b], dim=0
        )  # (B,4,D,H,W)  — visible channels only

        # Replace worst sample (per-task MSE) with a fresh seed for that task
        with torch.no_grad():
            per_sample = [
                F.mse_loss(
                    batch[i : i + 1, -vis:],
                    self.targets[task_ids_b[i]][:, -vis:],
                ).item()
                for i in range(bs)
            ]
            worst      = int(np.argmax(per_sample))
            reset_task = torch.tensor([task_ids_b[worst]], device=dev)
            batch[worst : worst + 1] = self.model.seed_center(1, dev, reset_task)

        # Forward + loss
        self.optimizer.zero_grad()
        state    = self.model(batch, steps=n_steps)
        pred_vis = state[:, -vis:]
        tgt_vis  = tgt_batch[:, -vis:]
        pred_a   = pred_vis[:, -1:]
        tgt_a    = tgt_vis[:, -1:]
        tgt_mask = (tgt_a > cell_cfg.alive_threshold).float()

        loss_alpha    = F.mse_loss(pred_a, tgt_a)
        color_diff    = (pred_vis[:, :-1] - tgt_vis[:, :-1]) ** 2 * tgt_mask
        loss_color    = color_diff.sum() / tgt_mask.sum().clamp(min=1.0)
        loss_overflow = ((pred_a * (1.0 - tgt_mask)) ** 2).mean()

        loss = 4.0 * loss_alpha + 1.0 * loss_color + 2.0 * loss_overflow
        loss.backward()
        torch.nn.utils.clip_grad_norm_(self.model.parameters(), 1.0)
        self.optimizer.step()
        self._lr_scheduler.step()

        # Write updated states back to pool
        with torch.no_grad():
            for j, idx in enumerate(indices):
                self._pool[idx] = state[j : j + 1].detach()
        self.state = state[0:1].detach()

        return {
            "loss_total":    loss.item(),
            "loss_alpha":    loss_alpha.item(),
            "loss_color":    loss_color.item(),
            "loss_overflow": loss_overflow.item(),
        }

    def _prep(self, t: np.ndarray, vis: int) -> Tensor:
        """(D,H,W,C) -> (1,C,D,H,W) on device. Visible channels only for loss."""
        t_chw = np.transpose(t, (3, 0, 1, 2)).astype(np.float32)
        return torch.from_numpy(t_chw).unsqueeze(0).to(self._device)

