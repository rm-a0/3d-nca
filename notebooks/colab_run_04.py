def _rgba(mask: np.ndarray, r: float, g: float, b: float) -> np.ndarray:
    """Boolean (D,H,W) mask -> (D,H,W,4) RGBA float32. Alpha = filled voxel."""
    out = np.zeros((*mask.shape, 4), dtype=np.float32)
    out[mask, 0] = r
    out[mask, 1] = g
    out[mask, 2] = b
    out[mask, 3] = 1.0
    return out

def make_tetrahedron(grid: int = GRID, pad: int = 3) -> np.ndarray:
    """Regular tetrahedron inscribed in the unit cube."""
    n = grid - 2 * pad
    i, j, k = np.mgrid[0:n, 0:n, 0:n]
    x = ((i + 0.5) / n) * 2.0 - 1.0
    y = ((j + 0.5) / n) * 2.0 - 1.0
    z = ((k + 0.5) / n) * 2.0 - 1.0
    s = 0.85
    mask_raw = (
          (x + y + z <= s)
        & (-x - y + z <= s)
        & (-x + y - z <= s)
        & ( x - y - z <= s)
    )
    full = np.zeros((grid, grid, grid), dtype=bool)
    full[pad : pad + n, pad : pad + n, pad : pad + n] = mask_raw
    return _rgba(full, r=0.35, g=0.55, b=1.0)

def make_cube(grid: int = GRID, pad: int = 5) -> np.ndarray:
    """Solid axis-aligned cube centred in the grid."""
    full = np.zeros((grid, grid, grid), dtype=bool)
    full[pad : grid - pad, pad : grid - pad, pad : grid - pad] = True
    return _rgba(full, r=1.0, g=0.6, b=0.2)


def make_sphere(grid: int = GRID) -> np.ndarray:
    """Solid sphere centred in the grid."""
    cx = cy = cz = grid / 2.0
    r  = grid / 2.0 - 2.0
    i, j, k = np.mgrid[0:grid, 0:grid, 0:grid]
    mask = (i - cx) ** 2 + (j - cy) ** 2 + (k - cz) ** 2 <= r ** 2
    return _rgba(mask, r=0.2, g=0.9, b=0.4)

